module.exports = function(RED) {
    function filterJson(config) {

        RED.nodes.createNode(this,config);
     	this.key = config.key;
        var node = this;
		
		node.on('input', function(msg) {
		
			node.send(msg);
  				
		});
		
		node.on('close', function() {
		});
    }
    RED.nodes.registerType("filterJson",filterJson);
}